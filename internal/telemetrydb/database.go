package telemetrydb

var TDB_COUNT_CREATE=`
CREATE TABLE IF NOT EXISTS Counters (
  key         TEXT PRIMARY KEY,
  timestamp   INTEGER,
  value       INTEGER
)
`
var TDB_METRIC_CREATE=`
CREATE TABLE IF NOT EXISTS Metric (
  key         TEXT,
  timestamp   INTEGER,
  value       TEXT
)
`
var TDB_HISTORY_CREATE=`
CREATE TABLE IF NOT EXISTS History (
  id          INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
  key         TEXT,
  timestamp   INTEGER,
  value       TEXT
)
`
var TDB_LOG_CREATE=`
CREATE TABLE IF NOT EXISTS Log (
  id          INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
  key         TEXT,
  timestamp   INTEGER,
  value       TEXT
)
`

var TDB_COUNT_IX1_CREATE=`CREATE INDEX IF NOT EXISTS CNT_IX1  ON Counters (timestamp)`
