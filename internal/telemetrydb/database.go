var TDB_METRIC_IX2_CREATE=`CREATE UNIQUE INDEX IF NOT EXISTS MET_IX2 ON Metric (key)`
var TDB_HISTORY_IX1_CREATE=`CREATE INDEX IF NOT EXISTS HST_IX1 ON History (timestamp)`
var TDB_HISTORY_IX2_CREATE=`CREATE INDEX IF NOT EXISTS HST_IX2 ON History (key)`
var TDB_LOG_IX1_CREATE=`CREATE INDEX IF NOT EXISTS LOG_IX1 ON Log (timestamp)`
var TDB_LOG_IX2_CREATE=`CREATE INDEX IF NOT EXISTS LOG_IX2 ON Log (key)`

